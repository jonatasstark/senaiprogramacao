#include <stdio.h> // biblioteca usada para os comandos de entrada printf e saída scanf

int main() { // funçao main que faz parte do programa principal
    int a = 5; // foi criada uma variável "a" com o valor 5
    int b = 3; // foi criada uma variável "b" com o valor 3
    int result; // foi criada uma variável result
    
    result = a - b; // result vai receber o valor de 5 - 3 que é 2

    printf("O resultado da subtração é: %d\n", result); // imprimi na tela o resultado da subtração = 2

    return 0; // retorna o valor 0 (verdadeiro) e encerra o programa
} // fim do programa principal